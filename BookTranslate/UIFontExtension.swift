import Foundation
import UIKit

extension UIFont {
    static var SFProMedium: UIFont? {
        return UIFont(name: "SFPro-Medium", size: 24)
    }
    
    static var SFProRegular: UIFont? {
        return UIFont(name: "SFPro-Regular", size: 14)
    }
    
    static var buttonFont: UIFont? {
       return UIFont(name: "SFPro-Regular", size: 9.2)
    }

}
